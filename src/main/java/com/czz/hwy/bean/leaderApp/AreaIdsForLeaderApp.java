package com.czz.hwy.bean.leaderApp;

public class AreaIdsForLeaderApp {

    private String areaIds;
    
    private String areaNames;
    
    private int days;
    
    private String attendancesAlias;
    
    private String attendancesClass;
    
    private int beforeDayForGramsAVG;

    public String getAreaIds() {
        return areaIds;
    }

    public void setAreaIds(String areaIds) {
        this.areaIds = areaIds;
    }

    public String getAreaNames() {
        return areaNames;
    }

    public void setAreaNames(String areaNames) {
        this.areaNames = areaNames;
    }

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

    public String getAttendancesAlias() {
        return attendancesAlias;
    }

    public void setAttendancesAlias(String attendancesAlias) {
        this.attendancesAlias = attendancesAlias;
    }

    public String getAttendancesClass() {
        return attendancesClass;
    }

    public void setAttendancesClass(String attendancesClass) {
        this.attendancesClass = attendancesClass;
    }

    public int getBeforeDayForGramsAVG() {
        return beforeDayForGramsAVG;
    }

    public void setBeforeDayForGramsAVG(int beforeDayForGramsAVG) {
        this.beforeDayForGramsAVG = beforeDayForGramsAVG;
    }
    
    
}
